function searchItems(query) {
    const items = document.querySelectorAll('.item');
    const sections = document.querySelectorAll('.section');
    const modules = document.querySelectorAll('.module');
    
    if (!query.trim()) {
        items.forEach(item => item.classList.remove('hidden'));
        sections.forEach(section => section.classList.remove('hidden'));
        modules.forEach(module => module.classList.remove('hidden'));
        return;
    }
    
    const searchTerm = query.toLowerCase();
    
    items.forEach(item => {
        const itemName = item.dataset.itemId.toLowerCase();
        if (itemName.includes(searchTerm)) {
            item.classList.remove('hidden');
        } else {
            item.classList.add('hidden');
        }
    });
    
    modules.forEach(module => {
        const visibleItems = module.querySelectorAll('.item:not(.hidden)');
        const emptyModule = module.querySelector('.empty');
        if (visibleItems.length === 0 && !emptyModule) {
            module.classList.add('hidden');
        } else {
            module.classList.remove('hidden');
        }
    });
    
    sections.forEach(section => {
        const visibleModules = section.querySelectorAll('.module:not(.hidden)');
        if (visibleModules.length === 0) {
            section.classList.add('hidden');
        } else {
            section.classList.remove('hidden');
        }
    });
}

