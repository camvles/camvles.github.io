function createItemElement(item) {
    const itemDiv = document.createElement('div');
    itemDiv.className = 'item';
    itemDiv.draggable = true;
    itemDiv.dataset.itemId = item.id;
    itemDiv.title = item.name; // Add tooltip
    
    // Apply color class if item has a color
    if (item.color) {
        itemDiv.classList.add('color-' + item.color);
        console.log('Applied color class color-' + item.color + ' to item:', item.name);
    }
    
    const iconHtml = item.icon ? 
        '<img src="' + item.icon + '" class="item-icon" alt="' + item.name + '">' :
        '<div class="no-icon">?</div>';
    
    itemDiv.innerHTML = iconHtml + '<span>' + item.name + '</span>';
    
    // Item click events for selection
    itemDiv.addEventListener('click', function(e) {
        e.stopPropagation();
        if (this && this.dataset && this.dataset.itemId) {
            handleItemSelection(this, e.ctrlKey || e.metaKey, e.shiftKey);
        }
    });
    
    // Item right-click events for context menu
    itemDiv.addEventListener('contextmenu', function(e) {
        handleItemRightClick(e, this);
    });
    
    // Item drag events
    itemDiv.addEventListener('dragstart', function(e) {
        if (!this || !this.dataset || !this.dataset.itemId) return;
        
        // If this item isn't selected, clear selection and select only this item
        if (!selectedItems.has(this.dataset.itemId)) {
            clearSelection();
            selectItem(this);
        }
        
        draggedElement = this;
        draggedType = 'item';
        
        // Add dragging class to all selected items
        selectedItems.forEach(itemId => {
            const element = document.querySelector('[data-item-id="' + itemId + '"]');
            if (element) element.classList.add('dragging');
        });
        
        e.dataTransfer.setData('text/plain', JSON.stringify(Array.from(selectedItems)));
    });
    
    itemDiv.addEventListener('dragend', function() {
        // Remove dragging class from all selected items
        selectedItems.forEach(itemId => {
            const element = document.querySelector('[data-item-id="' + itemId + '"]');
            if (element) element.classList.remove('dragging');
        });
        
        if (draggedType === 'item') {
            draggedElement = null;
            draggedType = null;
        }
    });
    
    return itemDiv;
}

function setupModuleDragAndDrop(moduleDiv) {
    const itemsContainer = moduleDiv.querySelector('.items-container');
    
    // Item drop events
    itemsContainer.addEventListener('dragover', e => {
        if (draggedType === 'item') e.preventDefault();
    });
    
    itemsContainer.addEventListener('dragenter', () => {
        if (draggedType === 'item') moduleDiv.classList.add('drag-over');
    });
    
    itemsContainer.addEventListener('dragleave', (e) => {
        if (!itemsContainer.contains(e.relatedTarget)) {
            moduleDiv.classList.remove('drag-over');
        }
    });
    
    itemsContainer.addEventListener('drop', function(e) {
        if (draggedType === 'item') {
            e.preventDefault();
            moduleDiv.classList.remove('drag-over');
            
            const dataText = e.dataTransfer.getData('text/plain');
            const targetModule = parseInt(moduleDiv.dataset.module);
            const targetCategory = moduleDiv.dataset.category;
            
            try {
                const itemIds = JSON.parse(dataText);
                const movedItems = [];
                
                // Save state before moving items
                const itemNames = itemIds.map(id => {
                    const item = storageData.items.find(i => i.id === id);
                    return item ? item.name : 'Unknown item';
                });
                const description = itemIds.length === 1 ? 
                    'Move ' + itemNames[0] + ' to ' + targetCategory + ' Module ' + targetModule :
                    'Move ' + itemIds.length + ' items to ' + targetCategory + ' Module ' + targetModule;
                saveStateToHistory(description);
                
                itemIds.forEach(itemId => {
                    const item = storageData.items.find(i => i.id === itemId);
                    if (item) {
                        // Clear module name if moving to a different module or category
                        if (item.module !== targetModule || item.category !== targetCategory) {
                            delete item.moduleName;
                        }
                        item.module = targetModule;
                        item.category = targetCategory;
                        movedItems.push(item.name);
                    }
                });
                
                if (movedItems.length > 0) {
                    renderGrid();
                    updateStats();
                    clearSelection();
                    markAsChanged();
                    
                    const message = movedItems.length === 1 
                        ? 'Moved ' + movedItems[0] + ' to ' + targetCategory + ' Module ' + targetModule
                        : 'Moved ' + movedItems.length + ' items to ' + targetCategory + ' Module ' + targetModule;
                    showNotification(message);
                }
            } catch (e) {
                // Fallback for old single-item format
                const item = storageData.items.find(i => i.id === dataText);
                if (item) {
                    // Save state before moving item
                    saveStateToHistory('Move ' + item.name + ' to ' + targetCategory + ' Module ' + targetModule);
                    
                    // Clear module name if moving to a different module or category
                    if (item.module !== targetModule || item.category !== targetCategory) {
                        delete item.moduleName;
                    }
                    item.module = targetModule;
                    item.category = targetCategory;
                    renderGrid();
                    updateStats();
                    clearSelection();
                    markAsChanged();
                    showNotification('Moved ' + item.name + ' to ' + targetCategory + ' Module ' + targetModule);
                }
            }
        }
    });
    
    // Module drag events (on title only)
    const moduleTitle = moduleDiv.querySelector('.module-title');
    if (moduleTitle) {
        moduleTitle.addEventListener('dragstart', function(e) {
            if (!moduleDiv || !moduleDiv.dataset) return;
            
            draggedElement = moduleDiv;
            draggedType = 'module';
            moduleDiv.classList.add('dragging');
            e.dataTransfer.setData('text/plain', moduleDiv.dataset.module + '|' + moduleDiv.dataset.category);
        });
        
        moduleTitle.addEventListener('dragend', function() {
            if (moduleDiv) moduleDiv.classList.remove('dragging');
            if (draggedType === 'module') {
                draggedElement = null;
                draggedType = null;
            }
        });
    }
}

